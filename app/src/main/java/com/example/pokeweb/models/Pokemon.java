package com.example.pokeweb.models;

public class Pokemon {
    //Variables
    private String name;
    private String url;
    //Getters
    public String getName() {
        return name;
    }
    public String getUrl() {
        return url;
    }
    //Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setUrl(String url) {
        this.url = url;
    }
}
